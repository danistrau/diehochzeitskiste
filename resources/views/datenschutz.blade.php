                    <h3>Was genau sind Cookies? </h3>
                    Immer wenn Sie durch das Internet
                    surfen, verwenden Sie einen Browser. Bekannte Browser sind
                    beispielsweise Chrome, Safari, Firefox, Internet Explorer
                    und Microsoft Edge. Die meisten Webseiten speichern kleine
