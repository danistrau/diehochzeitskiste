                    Cookies. Eines ist nicht von der Hand zu weisen: Cookies
                    sind echt nützliche Helferlein. Fast alle Webseiten
                    verwenden Cookies. Genauer gesprochen sind es HTTP-Cookies,
                    da es auch noch andere Cookies für andere Anwendungsbereiche
                    gibt. HTTP-Cookies sind kleine Dateien, die von unserer
                    Webseite auf Ihrem Computer gespeichert werden. Diese
                    Cookie-Dateien werden automatisch im Cookie-Ordner, quasi
                    dem “Hirn” Ihres Browsers, untergebracht. Ein Cookie besteht
                    aus einem Namen und einem Wert. Bei der Definition eines
                    Cookies müssen zusätzlich ein oder mehrere Attribute
                    angegeben werden. Cookies speichern gewisse Nutzerdaten von
                    Ihnen, wie beispielsweise Sprache oder persönliche
                    Seiteneinstellungen. Wenn Sie unsere Seite wieder aufrufen,
                    übermittelt Ihr Browser die „userbezogenen“ Informationen an
                    unsere Seite zurück. Dank der Cookies weiß unsere Webseite,
                    wer Sie sind und bietet Ihnen die Einstellung, die Sie
                    gewohnt sind. In einigen Browsern hat jedes Cookie eine
                    eigene Datei, in anderen wie beispielsweise Firefox sind
                    alle Cookies in einer einzigen Datei gespeichert. Es gibt
                    sowohl Erstanbieter Cookies als auch Drittanbieter-Cookies.
                    Erstanbieter-Cookies werden direkt von unserer Seite
                    erstellt, Drittanbieter-Cookies werden von Partner-Webseiten
                    (z.B. Google Analytics) erstellt. Jedes Cookie ist
                    individuell zu bewerten, da jedes Cookie andere Daten
                    speichert. Auch die Ablaufzeit eines Cookies variiert von
                    ein paar Minuten bis hin zu ein paar Jahren. Cookies sind
                    keine Software-Programme und enthalten keine Viren, Trojaner
